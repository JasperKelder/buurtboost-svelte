<h2>Scenario's</h2>
<p>
	We betrekken een actieve groep meedenkers en gaan daarmee dieper in op het BuurtRapport. We maken
	nu ook de koppeling tussen sociale en fysieke uitdagingen. Met woningcorporaties en gemeentes
	werken we aan scenario’s voor bijvoorbeeld renovatie en verduurzaming, en nieuwbouw met maximaal
	behoud van bestaande woningen. In een afwegingsmatrix zetten we sociale impact voor de bewoners,
	ruimtelijke kwaliteit, financiële haalbaarheid en procesrisico’s tegenover elkaar. De matrix laat
	zien welk scenario het best past bij de vragen die er leven én ook nog haalbaar is. Daarmee kiezen
	we het gewenste BuurtScenario.
</p>
